# Para usar paquetes se debe crear una carpeta y colocar alli dentro
# crear un archivo __init__ desde sublime, eso le indica que esa carpeta 
# es un paquete
# se pueden crear varios sub paquetes como se quiera organizar
# siempre creando el archivo __init__ dentro de cada carpeta

def sumar(op1, op2):
	print("La suma da: ", op1+op2)

def restar(op1,op2):
	print("La resta da: ", op1-op2)

def multiplicar(op1,op2):
	print("La multiplicacion da: ", op1*op2)

def dividir(dividendo,divisor):
	print("La division da: ", dividendo/divisor)